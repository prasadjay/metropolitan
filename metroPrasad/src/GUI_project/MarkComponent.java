      public String getDateInString(Date d)
    {
     
        String year = Integer.toString(d.getYear()+1900);
        String month = Integer.toString(d.getMonth()+1);
        String date = Integer.toString(d.getDate());
        
        String temp = year+"/"+month+"/"+date+"/";
        return temp;
    }
    
